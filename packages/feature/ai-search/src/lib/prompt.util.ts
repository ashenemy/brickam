/**
 * Строит промпт для LLM (Foundations §13). Инструктирует вернуть ТОЛЬКО строгий
 * JSON формы `{ projectType, themes: [{ name, materialCategories, keywords }] }`
 * с не более чем `maxThemes` темами, опираясь на описание проекта `text`.
 * Чистая функция (без побочных эффектов) — легко тестируется.
 */
export function buildPrompt(text: string, maxThemes: number): string {
    const limit = Math.max(1, maxThemes);
    return [
        'Ты — ассистент по подбору строительных материалов на маркетплейсе BuildHub.',
        'По описанию проекта пользователя определи тип проекта и разбей задачу на темы',
        'подбора материалов (например: фундамент, кровля, отделка стен).',
        '',
        'Верни СТРОГО валидный JSON и НИЧЕГО больше — без markdown, пояснений и текста вокруг.',
        'Форма ответа:',
        '{',
        '  "projectType": "краткий тип проекта",',
        '  "themes": [',
        '    {',
        '      "name": "название темы",',
        '      "materialCategories": ["slug-категории", "..."],',
        '      "keywords": ["ключевое слово", "..."]',
        '    }',
        '  ]',
        '}',
        '',
        `Требования: не более ${limit} тем; у каждой темы минимум одно ключевое слово;`,
        'keywords и materialCategories — про строительные материалы.',
        '',
        `Описание проекта: """${text}"""`,
    ].join('\n');
}
